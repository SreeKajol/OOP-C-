#include <bits/stdc++.h>
using namespace std;
template <class T>
void print(T t)
{
    cout << t << endl;
}
template <class T>
void print(T t, int n)
{
    for (int i = 0; i < n; i++)
        cout << t << " ";
    cout << endl;
}
int main()
{
    print(1);
    print(3.4);
    print(455, 3);
    print(" allaha ho akbar", 3);

    return 0;
}
